package symo.pl;

public class Movie {

	private int id;  
	private String name;  

	public int getId() {  
		return id;  
	}  
	public void setId(int id) {  
		this.id = id;  
	}  
	public String getName() {  
		return name;  
	}  
	public void setName(String firstName) {  
		this.name = firstName;  
	}  

}
