package symo.pl;

public class Movie {
		
	private String uuid;  
	private String name;
	private String owner; 

	public String getUuid() {  
		return uuid;  
	}  
	public void setUuid(String uuid) {  
		this.uuid = uuid;  
	}  
	public String getName() {  
		return name;  
	}  
	public void setName(String name) {  
		this.name = name;  
	}  
	public String getOwner() {  
		return owner;  
	}  
	public void setOwner(String owner) {  
		this.owner = owner;  
	}  

}
